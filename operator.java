package agelimit;

public class operator {
    public static void main(String[] args) {

        String n="Automation testing";
        System.out.println(n);

        int i=8;
        int j=4;
        System.out.println(i-j);

        float f=0.8f;
        float p=7.8f;
        System.out.println(f+p);

        double d=60.45678798;
        double e=50.54767889;
        System.out.println("double:"+ d*e);

        char a=90;
        char b=10;
        System.out.println("Character:"+ a/b);

        long g=50000000l;
        System.out.println("Long:"+ g++);
        System.out.println("Long:"+ ++g);

        short s=67;
        System.out.println("Short:"+ s--);
        System.out.println("Short:"+ --s);



    }
}
